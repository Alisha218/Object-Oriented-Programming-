  String BankCardNumber = cn.getText();
        String TransferAmmount = ta.getText();

        if (BankCardNumber.isEmpty() || TransferAmmount.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double amount = Double.parseDouble(TransferAmmount);
            saveToDatabase(BankCardNumber, amount);
            JOptionPane.showMessageDialog(this, "Transaction Successful", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid amount", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        ConfirmandTicketGen ctg=new ConfirmandTicketGen();
        ctg.setVisible(true);
        this.dispose();
    


